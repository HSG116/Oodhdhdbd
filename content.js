function عرض_المحتوى(القسم) {
    const contentArea = document.getElementById("المحتوى");
    if (contentArea) {
        contentArea.innerHTML = المحتويات[القسم].map(نص => `
            <p class="fade-in">${نص}</p>
        `).join('');
    }
}

const الأدعية_اليومية = [
    "اللهم إني أسألك حسن الخاتمة",
    "اللهم ارزقني لذة النظر إلى وجهك الكريم",
    "اللهم اجعل القرآن ربيع قلبي ونور صدري"
];

function generateDailyDua() {
    const randomIndex = Math.floor(Math.random() * الأدعية_اليومية.length);
    const dailyDuaText = document.getElementById('dailyDuaText');
    if (dailyDuaText) {
        dailyDuaText.textContent = الأدعية_اليومية[randomIndex];
    }
}
